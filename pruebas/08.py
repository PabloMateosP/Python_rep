# VARIABLES MULTI VALOR 
x, y, z = "Orange", "Green", "Purple" 

print(x)
print(y)
print(z)

# ONE VALUE TO MULTIPLE VARIABLES 
f = m = n = "Marble"

print(z)
print(m) # Print: Marble 
print(n)

# UNPACK A COLLECTION 
fruits = ["melon", "watermelon", "tomatoe"]
p, o, l = fruits
print(p) # Print: melon
print(o) # Print: watermelon
print(l) # Print: tomatoe
