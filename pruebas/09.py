# OUTPUT VARIABLES 
# The python print() function is often used to output variables 

# You can output multiple variables separated by ',' or '+'
x = "Python"
y = "is"
z = "awesome"
print(x, y, z) # the space between the comas must appear

x = "Python"
y = "is"
z = "awesome"
print(x + y + z)

# For numbers the '+' works as a mathematical operator
n = 5 
m = 10 
print(n + m) # Will be 15 